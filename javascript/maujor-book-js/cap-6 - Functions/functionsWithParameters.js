function calculoRetangulo() {
    var area = 7 * 4;
    var perimetro = (7 + 4) * 2;

    alert(`A area do retangulo é ${area} e o perimetro é ${perimetro}`)

}

function calculaRetanguloQualquer(a, h) {
    var area  = a * h;
    var perimetro = (a + h) * 2;

    alert(`A area do retangulo informado é ${area} e o perimetro é ${perimetro}`)
}