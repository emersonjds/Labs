// && (AND) verify both values are true and return, if not return false
var x = 3;
var y = 7;

x === 3; // return true, verify value and type

y === 5; // return false, verify value and type

x === 3 && y === 7; // return true, verify value and type

x === 3 && y === 4; // return false, if any variable has a diferent value the && operator return false, just return true with both variable   are true 

// || (OR) verify of any value is true, if yes return true, just return false with both values false

x === 3 || y === 3

// !(NOT) inverts a value
x === 3; // return true
!x === 3; // return falsea

x != 3; //return false
